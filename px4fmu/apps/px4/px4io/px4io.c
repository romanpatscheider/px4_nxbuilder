int px4io_main(int argc, char *argv[])
{
      struct i2c_dev_s *bus = NULL;
      int ret = -1;

      /* get a bus instance for I2C2 */
      bus = up_i2cinitialize(2);
      if (!bus)
            goto out;


      /* XXX do stuff here */
      

out:
      if (bus)
            up_i2cuninitialize(bus);
      return ret;
}
